/*----------------------------------------
程序中臨時需要一個小型的表格做處理
例如一個小型的數值清單
一般會建實體表, temp table, 或是變數表
這裡舉例用實體表
----------------------------------------*/
drop table if exists dbo.PhysicalTable;
create table dbo.PhysicalTable
(MyNumber int);

insert into dbo.PhysicalTable (MyNumber)
VALUES (1),(2),(3),(4),(5);

select PhysicalTable.MyNumber 
from dbo.PhysicalTable;
go


