/*----------------------------------------
可以用在 FROM 的話, 那可以用在 JOIN 嗎? 
可以的, 但有限制是他本身就是使用 alias name
所以不能再給別名了

例如有一個查詢如下
PS. 如果你的AdventureWorks是別的版本, 
    請自行調整filter條件以符合該版本
----------------------------------------*/
use AdventureWorks2025;
go

select 
	*
from 
	Sales.SalesOrderHeader as soh
join 
	Sales.SalesOrderDetail as sod
	on soh.SalesOrderID = sod.SalesOrderID
where 
	soh.ShipDate between '2024-01-01' and '2025-12-31';



